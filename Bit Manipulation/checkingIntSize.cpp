#include <bits/stdc++.h>
#define ll long long 
#define all(x) x.begin(), x.end()

using namespace std;
/*
BITWISE OPERATOR
& -> AND OPERATOR WORKS ON BINARY FORMS
| -> OR OPERATOR WORKS ON BINARY FORMS
~ -> INVERT OPERATOR WORKS ON BINARY FORMS
^ -> XOR OPERATOR WORKS ON BINARY XOR
<< -> LEFT SHIFT OPERATOR. ADDS 0 AT THE END OF THE BINARY FORM.EX:  value << number of bits
>> -> RIGHT SHIFT OPERATOR. REMOVE LAST DIGIT OF THE BINARY FORM.EX: value >> number of bits
*/
int main(){
    cout<<INT_MAX<<endl;
    int a = (1LL<<31)-1;
    cout<<a<<endl;
    unsigned int b = (1LL<<32)-1;
    cout<<b<<endl;   
}